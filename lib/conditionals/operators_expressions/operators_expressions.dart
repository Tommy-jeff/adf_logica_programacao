void main() {
  /// Operators:
  /// > greatter than
  /// >= greatter than or equal to
  /// < less than
  /// <= less than or equal to
  /// == equal to
  /// != different from

  /// Relational expressions:
  /// 2 > 1 result: true
  /// 1 > 2 result: false
  /// 2 >= 1 result: true
  /// 1 >= 2 result: false
  /// 1 < 2 result: true
  /// 2 < 1 result: false
  /// 1 <= 2 result: true
  /// 2 <= 1 result: false
  /// 1 == 1 result: true
  /// 1 == 2 result: false
  /// 1 != 2 result: true
  /// 1 != 1 result: false

  print(2 > 1);
  print(1 > 2);
  print(2 >= 1);
  print(1 >= 2);
  print(1 < 2);
  print(2 < 1);
  print(1 <= 2);
  print(2 <= 1);
  print(1 == 1);
  print(1 == 2);
  print(1 != 2);
  print(1 != 1);
}
